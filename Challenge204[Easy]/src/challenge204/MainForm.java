    private void btn_LoadFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_LoadFileActionPerformed
	int returnVal = jFileChooser1.showOpenDialog(this);
	if (returnVal == jFileChooser1.APPROVE_OPTION) {
	    File file = jFileChooser1.getSelectedFile();
	    try {
		jLabel1.setText(file.getAbsolutePath());

		BufferedReader reader = new BufferedReader(new FileReader(file.getAbsolutePath()));
		
		String phraseChunk = "";
		for (String line = reader.readLine(); line != null; line = reader.readLine()) {
		    if(line.isEmpty()){
			phraseList.add(phraseChunk);
			phraseChunk = "";
		    }else {
			// Adds stripped newline
			phraseChunk = phraseChunk +"\n"+ line;
		    }
		}
		// Adds the last line
		phraseList.add(phraseChunk);

	    } catch (IOException ex) {
		System.out.println("problem accessing file" + file.getAbsolutePath());
	    }
	} else {
	    System.out.println("File access cancelled by user.");
	}
    }//GEN-LAST:event_btn_LoadFileActionPerformed

