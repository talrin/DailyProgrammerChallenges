    private void btn_FindPhraseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_FindPhraseActionPerformed
	if(!txt_Phrase.getText().isEmpty()){
	    for(String phrase : phraseList){
		if(phrase.contains(txt_Phrase.getText())){
		    txtArea_DisplayPhrase.setText(phrase);
		    return;
		}
	    }
	    // Search Failed
	    txtArea_DisplayPhrase.setText("Phrase Not Found");
	}
    }//GEN-LAST:event_btn_FindPhraseActionPerformed

